# Operações matemáticas (Aritiméticas)
"""
    - Soma: +
    - Subtração: -
    - Multiplicação: *
    - Divisão: /
    - Divisão inteira: //
    - Resto da divisão: %
    - Potência: **
"""

numero1 = 10
numero2 = 2

print(numero1+numero2, f'-> Soma: {numero1}+{numero2}')
print(numero1-numero2, f'-> Subtração: {numero1}-{numero2}')
print(numero1*numero2, f'-> Multiplicação: {numero1}*{numero2}')
print(numero1/numero2, f'-> Divisão: {numero1}/{numero2}')
print(numero1//numero2, f'-> Parte inteiro da divisão: {numero1}//{numero2}')
print(numero1%numero2, f'-> Resto da divisão: {numero1}%{numero2}')
print(numero1**numero2, f'-> Potência: {numero1}**{numero2}')

# Operadores Booleanos - Lógicos
"""
    == Igualdade
    != Diferença
    > Maior
    >= Maior igual
    < Menor
    <= Menor igual
"""
print(numero1 == numero2, f'Igualdade, {numero1},{numero2}')
print(numero1 != numero2, f'Diferença, {numero1},{numero2}')
print(numero1 > numero2, f'Maior, {numero1},{numero2}')
print(numero1 >= numero2, f'Maior igual, {numero1},{numero2}')
print(numero1 < numero2, f'Menor, {numero1},{numero2}')
print(numero1 <= numero2, f'Menor igual, {numero1},{numero2}')